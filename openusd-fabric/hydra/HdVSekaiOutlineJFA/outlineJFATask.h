// HdVSekaiOutlineJFATask -- Hydra task that draws V-Sekai MToon outlines
// via the Jump Flood Algorithm (CHI-255) on top of the resolved scene
// colour AOV. Mirrors the Godot CompositorEffect and Unity URP
// ScriptableRenderPass in this repo so all three engines run the same
// three-pass dispatch:
//
//   1. silhouette_init  - seeds the JFA buffer from a rasterized
//                         (material_id, width_px) AOV the application
//                         renders during the main pass.
//   2. jfa_step         - one ping-pong JFA iteration; dispatched
//                         ceil(log2(maxOutlineWidthPx)) times with the
//                         stride halved each iteration.
//   3. final            - distance threshold + outline composite.
//
// The three GLSL kernels are the slangc output of
// shaders/outline_jfa/*.slang; they are embedded as string literals in
// outlineJFATask.cpp so the plugin has no runtime file dependency. The
// `pixi run emit-shader-glsl` task regenerates those literals; CI diffs
// the regenerated bytes against the embedded ones to catch drift
// between the Slang source-of-truth and the shipped GLSL.

